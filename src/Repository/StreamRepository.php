    public function findStreamsOfTomorrow(): array
    {
        $tomorrow = new \DateTime('tomorrow');

        return $this->createQueryBuilder('s')
            ->where('s.date = :tomorrow')
            ->setParameter('tomorrow', $tomorrow)
            ->getQuery()
            ->getResult();
